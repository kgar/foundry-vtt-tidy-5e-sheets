      justify-content: space-between;
      align-items: center;
      gap: 0.25rem;
      font-size: 0.75rem;
      line-height: 1rem;
      padding: 0.1875rem 0 0.125rem 0;
    }
  }

  .tidy-sheet-body {
    :global(.item-table-cell:not(.primary) input) {
      text-align: center;
    }

    :global(.item-table-cell input) {
      height: 1.5rem;
    }
  }
</style>
