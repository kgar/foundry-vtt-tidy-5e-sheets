  header {
    display: flex;
    flex-direction: row;
    gap: 1rem;
    justify-content: center;
    padding: 0.625rem 1rem 1rem 1rem;
    background: var(--t5e-header-background);

    .actor-name-row {
      margin-bottom: 0.125rem;
    }

    .origin-summary {
      margin-left: 0.25rem;
